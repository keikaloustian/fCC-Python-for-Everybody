# RELATIONAL DBs

# Storing of data in rows and columns in tables.
# Have the power to efficiently retrieve data in particular where there are multiple tables and the relationships between the tables are involved in the query.


# TERMINOLOGY
# Database - contains tables
# Relation / Table - made up of rows and columns
# Tuple / Row - a set of fields that generally represents an object
# Attribute / Column / Field - one of possibly many properties assigned to the object in question


# STRUCTURED QUERY LANGUAGE (SQL)
# Standard language for issueing commands to the database.
# There's SQL that is common to most vendors and products and portions that are unique to each.