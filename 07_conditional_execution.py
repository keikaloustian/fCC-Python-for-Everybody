# IF STATEMENT
x = 5
if x < 10:
    print('smaller')
if x > 10:
    print('bigger')

# One-line notation
if x % 2 == 0:  print('even')

# NEGATIVE
if not x == 200:
    print('Not 200')

# COMPARISON OPERATORS
# Less than <
# Less than <
# Greater than >
# Greater than or equal >=
# Equal ==
# Not equal !=


# IF ELSE STATEMENT
y = 4
if y > 2:
    print('bigger')
else:
    print('smaller')
